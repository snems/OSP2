def isCharColumEmpty(image, x, y):
    level = 64
    i = 0
    result = True
    for i in range(int(char_size_y)):
        r, g, b = image.getpixel((int(x), int(y + i)))
        if (r > level or g > level or b > level):
            result = False
            break

    return result


